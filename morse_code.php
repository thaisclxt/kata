<?php
    function decode_morse(string $code): string {
        $words = explode("  ", $code);
        $encoded = [];

        foreach($words as $word) {
            $letters = explode(" ", $word);

            foreach($letters as $char) {
                $encoded[] = match ($char) {
                    ".----" => "1",
                    "..---" => "2",
                    "...--" => "3",
                    "....-" => "4",
                    "....." => "5",
                    "-...." => "6",
                    "--..." => "7",
                    "---.." => "8",
                    "----." => "9",
                    "-----" => "0",
                    ".-" => "a",
                    "-..." => "b",
                    "-.-." => "c",
                    "-.." => "d",
                    "." => "e",
                    "..-." => "f",
                    "--." => "g",
                    "...." => "h",
                    ".." => "i",
                    ".---" => "j",
                    "-.-" => "k",
                    ".-.." => "l",
                    "--" => "m",
                    "-." => "n",
                    "---" => "o",
                    ".--." => "p",
                    "--.-" => "q",
                    ".-." => "r",
                    "..." => "s",
                    "-" => "t",
                    "..-" => "u",
                    "...-" => "v",
                    ".--" => "w",
                    "-..-" => "x",
                    "-.--" => "y",
                    "--.." => "z",
                    default => " ",
                };
            }
        }

        return join($encoded);
    }

    echo decode_morse("..... ----- .....");